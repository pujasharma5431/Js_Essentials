//  Array Methods

let num=[1,2,3,4]
let b=num.toString()
console.log(b);


//  Join method in array

let c=num.join("_")
console.log(c);

// Pop Array
num.pop()
console.log(num)

// Push Array
num.push(0)
console.log(num)

//  unshift Array
num.unshift(22)
console.log(num)

// Shift Array
num.shift()
console.log(num)

//  Delete Array[It's Not a method but a operator]
delete num[0]
console.log(num);



